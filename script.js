function cekNilai() {

    const namaInput = document.getElementById("nama").value.trim().toLowerCase();
    const nrpInput = document.getElementById("nrp").value.trim();
    const hasil = document.getElementById("hasil");

    if (!namaInput || !nrpInput) {
        hasil.innerHTML = "Nama dan NRP harus diisi";
        return;
    }

    const mahasiswa = dataMahasiswa.find(m =>
        m.nama.toLowerCase() === namaInput && m.nrp === nrpInput
    );

    if (!mahasiswa) {
        hasil.innerHTML = "Data tidak ditemukan";
        return;
    }

    let totalBaris = 0;

    for (const kategori in mahasiswa.penilaian) {
        totalBaris += mahasiswa.penilaian[kategori].length;
    }

    let tabel = `
        <h3>Nama : ${mahasiswa.nama}</h3>

        <table>
        <tr>
        <th>Pertemuan</th>
        <th>Aspek Penilaian</th>
        <th>Skor</th>
        <th>Nilai Final</th>
        </tr>
        `;

    let nilaiFinalSudahDitampilkan = false;

    for (const kategori in mahasiswa.penilaian) {

        const aspekList = mahasiswa.penilaian[kategori];
        const rowspanKategori = aspekList.length;

        aspekList.forEach((item, index) => {

            tabel += "<tr>";

            if (index === 0) {
                tabel += `<td rowspan="${rowspanKategori}" class="kategori">${kategori}</td>`;
            }

            tabel += `
            <td>${item.aspek}</td>
            <td>${item.skor}</td>
            `;

            if (!nilaiFinalSudahDitampilkan) {
                tabel += `<td rowspan="${totalBaris}">${mahasiswa.nilaiFinal}</td>`;
                nilaiFinalSudahDitampilkan = true;
            }

            tabel += "</tr>";

        });

    }

    tabel += "</table>";

    let tabelKehadiran = `
        <h3>Riwayat Kehadiran</h3>

        <table>
        <tr>
        <th>Pertemuan</th>
        <th>Status</th>
        </tr>
        `;

    mahasiswa.kehadiran.forEach(k => {

        tabelKehadiran += `
        <tr>
        <td>${k.pertemuan}</td>
        <td>${k.status}</td>
        </tr>
        `;

    });

    tabelKehadiran += "</table>";

    hasil.innerHTML = tabel + tabelKehadiran;

}